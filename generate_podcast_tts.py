def main():
    print("=" * 60)
    print("开始生成播客 TTS 语音")
    print("=" * 60)
    print(f"文本长度: {len(text)} 字符")
    print()
    
    try:
        # 加载配置
        print("[1/3] 加载配置...")
        config = load_config()
        
        # 创建 TTS 客户端（使用豆包播客客户端）
        print("[2/3] 初始化 TTS 客户端...")
        tts_client = TTSClientFactory.create_doubao_podcast_client(timeout_seconds=300)
        
        # 生成输出路径
        output_dir = Path("out/tts_test")
        output_dir.mkdir(parents=True, exist_ok=True)
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        output_path = output_dir / f"podcast_final_{timestamp}.mp3"
        
        print(f"[3/3] 生成语音...")
        print(f"输出路径: {output_path}")
        print()
        print("⏳ 正在调用豆包 TTS API，请稍候...")
        print("   （文本较长，预计需要 1-2 分钟）")
        print()
        
        # 调用 TTS（使用 tts 模式）
        audio_path = tts_client.synthesize_to_file(
            text_or_ssml=text,
            output_path=str(output_path),
            mode="tts"  # 使用 TTS V3 HTTP 模式
        )
        
        print()
        print("=" * 60)
        print("✅ TTS 生成成功！")
        print("=" * 60)
        print(f"音频文件: {audio_path}")
        print(f"文件大小: {Path(audio_path).stat().st_size / 1024 / 1024:.2f} MB")
        print()
        print("你可以使用任意音频播放器打开该文件")
        
    except Exception as e:
        print()
        print("=" * 60)
        print("❌ TTS 生成失败")
        print("=" * 60)
        print(f"错误信息: {e}")
        import traceback
